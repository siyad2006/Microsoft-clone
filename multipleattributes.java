public class multipleattributes {
    int x=30;

    public static void main(String[] args) {
        multipleattributes mul1=new multipleattributes();
        multipleattributes mul2=new multipleattributes();
        System.out.println(mul1.x);
        mul2.x=50;
        System.out.println(mul2.x);
        
    }
}